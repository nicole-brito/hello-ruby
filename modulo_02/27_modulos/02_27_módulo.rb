#Módulos
#Módulos são coleções de métodos e atributos que podem ser utilizados em qualquer classe. São similares a classes, mas não possuem construtor. São utilizados para agregar funcionalidades a classes. 
#Podemos armazenar métodos, constantes, outros módulos e classes dentro de um módulo. A grande diferernça entre módulos e classes é que módulos não são instanciados e não possui hierarquia.

#PRINCIPAIS OBJETIVOS DO MÓDULO
#1. Organizar o código
    #Agem como namespace, para definir métodos cujos nomes não irão se colidir com aqueles definidos em outras partes do programa.
#2. Reutilizar código
    #Permite que você compartilhe funcionalidade entre classes - se uma classe "mistura" (MIXES IN) um módulo (ou seja, o inclui), todos os métodos de instância do módulo se tornam disponíveis como se tivessem sido definidos na classe
#3. Proteger o código
    #Evita que outros módulos ou classes acessem o código que está sendo encapsulado.


